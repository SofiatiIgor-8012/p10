# Criando uma tupla com as estacoes do ano
estacoes = ("Verão", "Outono", "Inverno", "Primavera")

# Exibindo a tupla completa
print(estacoes)

# Acessando elementos por indice (comeca em 0)
print(f"Primeira estacao: {estacoes[0]}")
print(f"Ultima estacao: {estacoes[-1]}")

# Verificando o tipo da variavel
print(f"Tipo: {type(estacoes)}")